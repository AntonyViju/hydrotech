                <p>Hard chrome bar is a chromium-plated steel product used primarily as piston rod material in all standard applications in hydraulics and pneumatics. Common applications are found in cranes, dump trucks, lifters, garbage compactors, agricultural machinery and production equipment with movable sections.</p>
