                            <p>Inspection : In the initial inspection, the rod is assessed to evaluate its condition ahead of the chroming process. Here the inspector will note any evidence of corrosion, pitting, marring, damage, cracking or flaking of the material. Presence of these phenomena will be taken into account and may influence the decisions taken at various steps along the re-chroming process.</p>
							<p>Grinding & Stripping : Before new chrome can be applied, the old material must be removed. However, this must be done in such a way to preserve the steel underneath and without reducing the diameter of the rod itself. Doing so would result in a rod that exceeds cylinder tolerances. There are two methods for removing this material. These are: grinding and stripping.</p>
							<p>Cleaning & Masking :
              Once the old chrome material has been removed, it is time to prepare the cylinder rod to be placed in the bath of chromium solution. Before this can happen, the rod must be thoroughly cleaned and any debris or contaminants must be removed. Failing to do this could result in undesirable anomalies during the plating process such as pitting and pinholes or flaking and peeling of the chrome during the life of the cylinder. At this stage, the rod is cleaned and the ends are masked to ensure a neat and professional final product.</p>
              <p>Plating : This is the part that most of us are familiar with. The process of electrolytic plating is a well-known one. It is used in everything from plating jewellery with gold to applying reflective metal plating to satellites and spacecraft. In this process, objects to be plated are placed in a bath containing a solution of dissolved metal molecules (in our case this metal is a special Chromium formulation). Electrodes are attached to the object to be plated and a positive current is applied to the solution. As the current runs through the solution and out through the electrodes, it causes the dissolved metal particles in the solution to be attracted to and adhere to the metal surface of the metal.</p>
