                            <p><strong>Double oil and filter change</strong></p>
							<p>This technique involves an initial oil drain and filter change, which expells a large percentage of contaminants and degraded oil. The system is then filled to the minimum level required and the oil circulated until operating temperature is reached and the fluid has been turned over at least five times. The oil is drained and the filters changed a second time. An appropriate oil analysis test should be performed to determine the success of the flush. To maximize the effectiveness of this technique, the system should be drained as thoroughly as possible and the reservoir mechanically cleaned.</p>
							<p><strong>Mechanical cleaning</strong></p>
							<p>Although not technically a flushing technique, the selective use of mechanical cleaning may be incorporated in the flushing strategy. This can involve the use of a pneumatic projectile gun to clean pipes, tubes and hoses (see exhibit 1), and disassembly of the reservoir and other components for cleaning using brushes and solvents. Mechanical cleaning is labor intensive and therefore costly. It carries with it reliability risks associated with opening the hydraulic system and intervention by human agents.</p>
							<p><strong>Power flushing</strong></p>
							Power flushing involves the use of a purpose-built rig to circulate a low viscosity fluid at high velocities to create turbulent flow conditions (Reynolds number > 2000). The flushing rig is typically equipped with a pump that has a flow rate several times that of system's normal flow, directional valves, accumulators, fluid heater and chiller and of course, a bank of filters. The directional valves enable the flushing direction to be changed, the accumulators enable pulsating flow conditions and the heater and chiller enable the fluid temperature to be increased or decreased, all of which can assist in the dislodgment of contaminants. Analysis of the flushing fluid is performed regularly during the flushing operation to determine the point at which the system has been satisfactorily cleaned.
