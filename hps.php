                            <p>Hydraulic Gear Pumps:<br>
Straight spur, helical, or herringbone gears are used in these pumps. Straight spur gears are commonly used and helical or herringbone gears are much expensive and run quietly. 
In gear pumps, the fluid is carried in between the two meshing gears. A shaft is used to drive the one gear and turns the other one idle. The pump housing and side plates will enclose the chambers formed between the gears.
Gear pumps can withstand pressure around 210 bar and will work at a high speed of 3000-6000 rpm. External gear pumps, internal gear pumps, screw pumps, and ring pump are the different classifications of gear pumps.</p>
                            <p>External Gear Pumps:<br>
Both industrial and mobile systems utilize these pumps for various applications as lubricating pumps in machine tools, fluid power transfer units and oil pumps in engines. Here, to interlock the teeth of gear pumps, one wheel is connected with the drive and another one will rotate in the opposite direction. Bearing block will help to position the wheels for a minimum clearance during teeth interlock. A lobe pump is a type of external gear pump that operates both lobes using drives situated outside the chamber. </p>
                        <p>Internal Gear Pumps:<br>
Internal gear pumps are versatile and can handle thick hydraulic fluids like asphalt, chocolate, and adhesives. They are used for applications like plastics and machine tools, presses, electric fork-lift, etc. These pumps can handle high viscosity and temperature ranges. Internal gear pumps can operate dry for short periods and are self-priming, non-pulsating and bi-directional. These gear pumps are reliable, easy to maintain and operate because of minimum moving parts. </p>  
