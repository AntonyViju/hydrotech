                <h4>HYDRAULIC PUMPS</h4>
                <p>A hydraulic pump is a mechanical source of power that converts mechanical power into hydraulic energy (hydrostatic energy i.e. flow, pressure). It generates flow with enough power to overcome pressure induced by the load at the pump outlet. When a hydraulic pump operates, it creates a vacuum at the pump inlet, which forces liquid from the reservoir into the inlet line to the pump and by mechanical action delivers this liquid to the pump outlet and forces it into the hydraulic system. Hydrostatic pumps are positive displacement pumps while hydrodynamic pumps can be fixed displacement pumps, in which the displacement (flow through the pump per rotation of the pump) cannot be adjusted, or variable displacement pumps, which have a more complicated construction that allows the displacement to be adjusted. Hydrodynamic pumps are more frequent in day-to-day life.</p>
