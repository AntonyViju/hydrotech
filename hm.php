                            <p>Gear-type hydraulic motor :<br>
                            The gear-type hydraulic motor consists of two gears, the idler gear, and the driven gear, that is attached to the output shaft. The high-pressure oil flows into one side around the periphery of the gears between the gear tips and the motor housing to the outlet port. The gears lock together, not allowing the oil from the outlet side to flow back to the inlet side.</p>
                            <p>Vane-type hydraulic motor :<br>
                            A vane motor consists of a housing with an eccentric bore, which runs a rotor with vanes that slide in and out. The force differential created by the unbalanced force of the pressurized fluid on the vanes causes the rotor to spin in one direction.</p>
                        <p>Radial-piston type hydraulic motor :<br>
