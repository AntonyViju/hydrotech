                            <p>Most hydraulic valves minimally consist of a main casing, a bonnet, a seat, and a disc. The main casing is the valve’s outer enclosure; it contains all the internal components, which are collectively called the trim. Most often, the casing is made from a metallic or plastic material. Common metallic materials include steel, stainless steel, alloyed steel, cast iron, bronze, brass, and gunmetal (red brass), while among the most common plastic options are PVC, PVDF, PP, and glass-reinforced nylon.</p>
                            <p>The bonnet is a semi-permanent, removable part of the valve that acts as a cover. For access to interior parts of the valve, the bonnet needs to be removed. Some valves do not have a bonnet because of the way they are constructed. (One example of such a valve is a plug valve.)

The term seat refers to the interior surface of the casing, which connects to the disc in order to create a leak-proof seal. The seat typically possesses sealing made of either rubber or plastic.

Finally, the disc (also called a valve member) is the part of the valve that slides into the seat to restrict flow and prevent leaking.</p>
