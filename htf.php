                <h4>HYDRAULIC FITTINGS</h4>
                <p>Hydraulic fittings connect conductors such as hoses, pipes and tubes in a hydraulic system. Most fittings have a male and female component that join to form a connection. These connections help contain and direct the flow of hydraulic fluid in the conductor while preventing leaks and maintaining pressure. Different fittings allow designers to change flow direction, the elevation of lines or split flow. Crimping is the most common method for assembling hoses and fittings. Once the crimp specifications have been checked for the specific hose and fitting, a crimping machine is adjusted to the appropriate crimping diameter setting. The next step is to lubricate the fitting, put it on the end of the hose, and then put it in the crimping machine.</p>
