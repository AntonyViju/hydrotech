                            <p>Hydraulic fittings are used to seal the hydraulic oil or fluid within the system. Thus, the fittings should be compatible with the fluid being used. Hydraulic fluid provides the energy to the system allowing the machinery to perform its work. There are different types of hydraulic media in the market.</p>
                            <p>Petroleum-based fluids:<br> Petroleum or mineral-based fluid is the most used hydraulic fluid. Additives in such fluids provide protection from rust and oxidation, wear and tear, corrosion, and extreme pressure. These fluids are generally high quality, low cost, and widely available.</p>
                        <p>Water-based fluids: <br>These fluids have high water content, so, they are used for fire-resistance. They are available as water-in-oil emulsions, oil-in-water emulsions, and water-glycol blends. They are more expensive than petroleum-based fluids and have lower wear resistance. </p>  
                        <p>Synthetic-based fluids: <br> These fluids are man-made and provide advantages like fire-resistance, lower friction, and thermal stability. They are ideal to use in high-temperature and high-pressure applications due to their excellent lubricating property. However, they are more expensive than petroleum-based fluids and maybe slightly toxic.</p>
