                <h4>HAND PUMPS FOR TANK MOUNTING SERIES PMA</h4>
                <p>The PMA series hand pumps are hydraulic pumps with lever control. The cycle is double pumped, it generates pressure with both lever movements. The pump displacement is for this reason the sum of the drives in both directions. This type of product is typically used in emergency lines inside hydraulic circuits. Available in displacements from 6 to 45 cm3/cycle (0,37 to 2,75 In3/cycle), the PMA hand pump is designed for mounting on the tank and is supplied with fixing screws and gasket. Depending on the displacement, the PMA hand pumps can be subjected to maximum pressures from 280 to 500 bar (4060 to 7250 PSI). Consult the technical catalog for optimal working pressures that allow you to balance the mechanical effort on the lever. The pump is equipped with a 4-way and 3-position flow diverter with lever control and open center spool, for feeding single-acting cylinders or reversible hydraulic motors. The pump body is in cast iron and its external surfaces are protected by black paint. The other components are made of steel and protected from oxidation with a galvanizing treatment. Optional functions are available upon request. The pump is supplied with an operating lever L=600 mm.</p>
